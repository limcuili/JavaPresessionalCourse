class HowAreYou {

    public static void main (String[] args) {
        System.out.print("Hello ");
        System.out.print(args[0]);
        System.out.println(", how are you today?");
    }

}

/*
To run the above in the terminal,
compile with 'javac HowAreYou.java',
run 'java HowAreYou Ellen Diana Tristan'.
Observe that it takes your arguments as an array.
*/
